export const NATURE_ORDER = { '吉': 0, '中性': 1, '凶': 2 }

export function sortedShensha(list) {
  if (!list?.length) return []
  const arr = Array.isArray(list) ? list : Object.values(list)
  return [...arr].sort((a, b) => (NATURE_ORDER[getShenshaInfo(a)?.nature] ?? 1) - (NATURE_ORDER[getShenshaInfo(b)?.nature] ?? 1))
}
